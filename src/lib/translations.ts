export const getDictionary = async (locale: string): Promise<Dictionary> => {
  const selectedDict = ispartaDictionary;
  
  return new Proxy(selectedDict, {
    get: (target, prop) => {
      const key = prop as keyof Dictionary;
      if (key in target) {
        return target[key];
      }
      
      return ispartaDictionary[key] || String(key);
    },
  }) as Dictionary;
};

